
import java.util.Scanner;

public class Inverso {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		
		System.out.println("Insira o Primeiro Número: ");
		int n1 = scan.nextInt();
		System.out.println("Insira o Segundo Número: ");
		int n2 = scan.nextInt();
		System.out.println("Insira o Terceiro Número: ");
		int n3 = scan.nextInt();
		System.out.println("Insira o Quarto Número: ");
		int n4 = scan.nextInt();
		System.out.println("Insira o Quinto Número: ");
		int n5 = scan.nextInt();
		System.out.println("Insira o Sexto Número: ");
		int n6 = scan.nextInt();
		System.out.println("Insira o Sétimo Número: ");
		int n7 = scan.nextInt();
		System.out.println("Insira o Oitavo Número: ");
		int n8 = scan.nextInt();
		System.out.println("Insira o Nono Número: ");
		int n9 = scan.nextInt();
		System.out.println("Insira o Décimo Número: ");
		int n10 = scan.nextInt();
		
		System.out.println("A impressão inversa dos seguintes números registrados:");

		System.out.println(n10);
		System.out.println(n9);
		System.out.println(n8);
		System.out.println(n7);
		System.out.println(n6);
		System.out.println(n5);
		System.out.println(n4);
		System.out.println(n3);
		System.out.println(n2);
		System.out.println(n1);
		
		scan.close();
		
	}

}
